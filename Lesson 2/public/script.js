var grassArr = [];
var xotakerArr = [];
var gishatichArr = [];
var XxyArr = [];
var QarakerArr = [];
var side = 10;
function setup() {
    frameRate(5);
    createCanvas(matrix[0].length * side, matrix.length * side);
    background('#acacac');
    for (y = 0; y < matrix.length; y++) {
        for (x = 0; x < matrix[y].length; x++) {
            if (matrix[y][x] == 1) {
                grassArr.push(new Grass(x, y, 1));

            }
            else if (matrix[y][x] == 2) {
                xotakerArr.push(new Xotaker(x, y, 2));
            }
            else if (matrix[y][x] == 3) {
                gishatichArr.push(new Gishatich(x, y));
            }
            else if (matrix[y][x] == 4) {
                XxyArr.push(new Xxy(x, y));
            }
            else if (matrix[y][x] == 6) {
                QarakerArr.push(new Qaraker(x, y));
            }
        }
    }
}


function draw() {
    noStroke();
    for (y = 0; y < matrix.length; y++) {
        for (x = 0; x < matrix[y].length; x++) {
            if (matrix[y][x] == 1) {
                fill("green");
            }
            else if (matrix[y][x] == 0) {
                fill("#acacac");
            }
            else if (matrix[y][x] == 2) {
                fill('yellow');
            }
            else if (matrix[y][x] == 3) {
                fill('red');
            }
            else if (matrix[y][x] == 4) {
                fill('white');
            }
            else if (matrix[y][x] == 5) {
                fill('black')
            }
            else if (matrix[y][x] == 6) {
                fill("#2A1EFF")
            }
            rect(x * side, y * side, side, side);


        }
    }

    for (var i in grassArr) {
        grassArr[i].bazmanal();
    }
    for (var n in xotakerArr) {
        xotakerArr[n].utel();
        xotakerArr[n].bazmanal();
        xotakerArr[n].mahanal();

    }
    for (var g in gishatichArr) {
        gishatichArr[g].utel();
        gishatichArr[g].bazmanal();
        gishatichArr[g].mahanal();
    }
    for (var m in XxyArr) {
        XxyArr[m].utel();
        XxyArr[m].bazmanal();
        XxyArr[m].mahanal();
    }
    for (var t in QarakerArr) {
        QarakerArr[t].utel();
        QarakerArr[t].bazmanal();
        QarakerArr[t].mahanal();
    }
}

